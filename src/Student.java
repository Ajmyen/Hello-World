public class Student {
    private int id;
    private String name;

    //getter
    public int getId() {
        return id;
    }

    //setter
    public void setId(int id) {
        this.id=id;
    }

    public String getName(){
        return name;
    }

    public void setName(String name) {
        this.name=name;
    }
}
